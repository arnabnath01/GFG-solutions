class Solution {
  public:
    long long sumMatrix(long long n, long long q) {
        long long maxi =n+n;
        if(q<=1 || q>maxi) return 0;
        
        if(q>maxi/2) return maxi-q+1;
        else return q-1;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        long long n,q;
        
        cin>>n>>q;

        Solution ob;
        cout << ob.sumMatrix(n,q) << endl;
    }
    return 0;
}
